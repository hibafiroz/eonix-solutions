//A database is a structured place where we store and organize data so we can find it update it or delete it easily
//its like a digital filing cabinet for information

//Database:

//A DBMS (Database Management System) is software acts as a bridge between the user and the database that helps to store, organize, manage the database
//and it supports CRUDS operations, ensuring data is organized, secure, and easily accessible.

//DBMS types:
//SQL:

//SQL databases are relational which means they store data in tables with rows and columns and the structure is fixed
//They require a schema, so every record must follow the same format. bcz of this structured nature, SQL databases are best for applications where data has clear relationships, such as banking, billing or ERP software
//SQL always use SQL commands.
//Examples: MySQL and PostgreSQL, oracle

//NoSQL:

//NoSQL databases are non-relational and flexible. They can store data in documents, key-value pairs, or graphs.
// They are schema-less, so each record can have different fields, and the structure can change anytime without affecting other data. This flexibility makes NoSQL databases more suitable for fast-growing, scalable applications like Instagram or e-commerce platforms.
// NoSQL mean not only sql but Use different query methods too
//Examples: MongoDB and Firebase, couchDB

//when to use:

//Use SQL when we need structured data, strong integrity and relationships like banking systems
//Use NoSQL when we need flexibility, speed and scalability for large data like social media feeds or real-time apps
